class Restaurant(object):

	def __init__(self, name, numVotes=0):
		self.name = name
		self.numVotes = numVotes

	def setName(name):
		self.name = name

	def getName():
		return self.name

	def setNumVotes(numVotes):
		self.numVotes = numVotes

	def addVotes(numVotes=1):
		self.numVotes += numVotes

	def getNumVotes():
		return self.numVotes